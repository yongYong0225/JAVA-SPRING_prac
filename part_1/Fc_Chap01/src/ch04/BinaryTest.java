package ch04;

public class BinaryTest {

    public static void main(String[] args) {

        int num = 10;
        int bnum = 0B1010; //2진수
        int oNum = 012; //8진수
        int xNum = 0xA; //10진수

        System.out.println(num);
        System.out.println(bnum);
        System.out.println(oNum);
        System.out.println(xNum);

    }
}
